local function updatePunch()
    local player = game:GetService("Players").LocalPlayer
    local punchValue = player.Backpack.Punch:FindFirstChildOfClass("NumberValue")
    if punchValue then
        punchValue.Value = noCD and 0 or punchCooldown
    end
